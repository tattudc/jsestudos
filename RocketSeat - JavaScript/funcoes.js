function soma(numero1, numero2){
    var resultado = numero1 + numero2
    return resultado
}
function subt(numero1, numero2){
    var resultado = numero1 - numero2
    return resultado
}
var resultado = soma(1, 2)
console.log(resultado)
var resultado = subt(15, 2)
console.log(resultado)
