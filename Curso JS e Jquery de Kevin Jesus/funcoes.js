//Funções é um grupo de responsabilidades dentro de uma caixa
//Função pode ou não retornar algo
//Exemplo de função sem parametro
function nomeDePrimeiraFuncao(){
    console.log(10+2)
}
//funcao com parametro
function nomeDaFuncao(valor){
    console.log(valor)
}
function media(nota1, nota2, nota3){
    console.log((nota1+nota2+nota3)/3)
}

nomeDePrimeiraFuncao()
nomeDaFuncao(25)
media(2, 10, 7)